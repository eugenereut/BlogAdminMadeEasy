<a href="writing.html" class="title parastyle">Три Слома Человечества</a>
<time>Апрель 15, 2017</time>
</header>
<div class="postbody">
<p>Не было никакого «осевого времени». Свою положительную роль эта идея сыграла: помогла вырваться из аутизма в христианской шкуре, помогла понять, что, как ни относиться к Иисусу,
  Он — не гром среди ясного неба, не прыщ на девичьей щеке, а Некто в Контексте. Что нимало не опасно для христианства, даже наоборот.</p>
<p>«Осевое время» не слишком удачная идея, потому что она представляет историю как единый процесс, в котором есть некоторая центральная «ось». Как у земного шара.</p>
<p>На самом деле, история человечества есть история по меньшей мере трёх сломов. Конечно, первичен не слом, первичен старт. Старт — прогресс — слом.</p>
<p>Первая эпоха — дописьменная. Общение уже появилось, уже стало специфически человеческим, не звериным, но ещё не совершён был прорыв к фиксации общения.
  Точнее, прорыв был, но сама фиксация оказалась несовершенной — это скульптура, живопись, могилы. Статуэтки, росписи, захоронения, — всё это способы зафиксировать некоторую мысль, идею.
  Понятно, насколько несовершенен этот способ, ведь мы не в силах понять, что хотел сказать автор палеолитической мадонны или палеолитического же фаллоса. Возможно, он и сам не вполне понимал, что говорил — до полной ясности мышления и коммуникации далеко и в наши дни.</p>
