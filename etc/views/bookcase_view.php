<a href="" class="title parastyle">Время и Пространство: От Крестьянской Архаики До Современности</a>
<time>Апрель 11, 2017</time>
</header>
<div class="postbody">
<p>Крестьянская мудрость гласит: «Земляной рупь тонок, а длинен, торговый широк, да короток». На самом деле, всё прямо наоборот: феодализм, аграрная цивилизация — это бег на короткую дистанцию, капитализм — это марафон.
  Крестьянин и все, кто с ним связаны, живёт одним годом, от посева от жатвы и от жатвы до посева.</p>
<p>Крестьянский мир — это мыльный пузырь, внутри которого бьётся поколения как мухи, и горе, когда этот пузырь лопается. Наступает голод и вымирание. Ничего нового нет и не может быть. Всё суета сует. Побеждает тот, кто дольше продержится на брыкающемся быке природы.
  Брыкания непредсказуемы как выигрышный лотерейный номер, но неизбежны. Развития нет, есть лишь умеренно разнообразные комбинации одних и тех же элементов.</p>
