<header>
<a href="" class="title parastyle">Давидград</a>
<time>Февраль 27, 2017</time>
</header>
<div class="postbody">
<p>В синодальном переводе в рассказе о нападении филистимлян на Иерусалим (2 Цар. 5, 17) перевод неточен: Давид-де «пошёл в крепость». В оригинале — Давид «спустился в крепость». Точнее, в оригинале игра словами: филистимляне «веялу», Давид «веярад»: филистимляне «поднимаются», так и по-русски можно сказать о врагах, тот же образ в слове «восстают»,
  а Давид «спускается», не нападает, не принимает бой, а скрывается в крепости.</p>
<p>Давид «спустился» с Храмовой горы, крепость была к югу от неё. То, что сегодня туристам показывают как башню Давида около Яффских ворот далеко к северу от Храмовой горы, это средневековое укрепление.</p>
<p>Вот для ясности схема Гаро Налбандяна, показывающая приблизительные границы «города Давида». Отдельно помечены места раскопок, проведедённых Эйлат Мацар (внучкой отца израильской археологии Беньямина Мацара) в 2000-е годы. Выделено то, что было Иерусалимом в правление Давида — будущая Храмовая гора и нижний, к югу от неё «город Давида».</p>
