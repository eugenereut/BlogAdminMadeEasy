<a href="" class="title parastyle">Вифавар: Перекрёсток Земли и Неба</a>
<time>Март 27, 2017</time>
</header>
<div class="postbody">
<p><em>«Это происходило в Вифаваре при Иордане, где крестил Иоанн»</em> (Ио. 1, 28).</p>
<hr>
<p>В западных изданиях, в современных русских переводах обычно ставят «Вифания». Тонкость в том, что гласных-то в еврейских текстах нет, так что возможны разные варианты, древние рукописи разногласят. На знаменитой мозаике, изображающей Палестину в VI  веке, место, где крестил Предтеча, обозначено именно как Вифавара.
  В 1994 году археологи обнаружили в селении Аль-Махтас руины византийского храма, который увековечивал именно место крещения Иисуса, с тех пор именно сюда ездят люди, желающие почтить место крещения Иисуса. Собственно «махтас» и означает «крещение» на арабском. Селение находится в 9 километрах к северу от Мёртвого моря, в 10 километрах от Иерихона.</p>
